// var : 재할당 가능
var vv = 123;
console.log(vv);
vv = 321;
console.log(vv);
console.log(`vv : ${vv}`);

// let : 재할당 가능
let ll = 123;
console.log(ll);
ll = 321;
console.log(ll);
console.log(`ll : ${ll}`);

// const : 재할당 불가능
const cc = 123;
console.log(cc);
cc = 321;
console.log(cc);
console.log(`cc : ${cc}`);